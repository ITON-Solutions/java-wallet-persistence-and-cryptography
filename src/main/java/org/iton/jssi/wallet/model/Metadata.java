package org.iton.jssi.wallet.model;

import com.j256.ormlite.field.DataType;
import com.j256.ormlite.field.DatabaseField;
import com.j256.ormlite.misc.BaseDaoEnabled;
import com.j256.ormlite.table.DatabaseTable;

@DatabaseTable(tableName = "metadata")
public class Metadata extends BaseDaoEnabled {
    @DatabaseField(columnName = "id", generatedId = true)
    private Integer id;
    @DatabaseField(columnName = "value", dataType = DataType.BYTE_ARRAY)
    private byte[] value;

    public Metadata() {
    }

    public Metadata(byte[] value) {
        this.value = value;
    }

    public Integer getId() {
        return id;
    }

    public byte[] getValue() {
        return value;
    }

    public void setValue(byte[] value) {
        this.value = value;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        if (!(object instanceof Metadata)) {
            return false;
        }
        Metadata other = (Metadata) object;
        return (this.id != null || other.id == null) && (this.id == null || this.id.equals(other.id));
    }

    @Override
    public String toString() {
        return "Metadata[ id=" + id + " ]";
    }
    
}
